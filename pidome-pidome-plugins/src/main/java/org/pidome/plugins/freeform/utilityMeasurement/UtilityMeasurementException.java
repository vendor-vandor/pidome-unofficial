package org.pidome.plugins.freeform.utilityMeasurement;

/**
 *
 * @author John
 */
public class UtilityMeasurementException extends Exception {

    /**
     * Creates a new instance of <code>UtilityMeasurementException</code>
     * without detail message.
     */
    public UtilityMeasurementException() {
    }

    /**
     * Constructs an instance of <code>UtilityMeasurementException</code> with
     * the specified detail message.
     *
     * @param msg the detail message.
     */
    public UtilityMeasurementException(String msg) {
        super(msg);
    }
}
